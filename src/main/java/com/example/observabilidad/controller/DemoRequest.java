package com.example.observabilidad.controller;

public class DemoRequest {
    private String nombre;
    private int edad;

    public DemoRequest() {}

    public DemoRequest(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
}
